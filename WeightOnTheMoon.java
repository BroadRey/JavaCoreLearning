public class WeightOnTheMoon {
    public static void main(String[] args) {
        int weightOnEarth, weightOnMoon;

        weightOnEarth = 65;
        weightOnMoon = (int) (weightOnEarth * 0.17);

        System.out.println("Мой вес на Земле: " + weightOnEarth);
        System.out.println("Мой вес на Луне: " + weightOnMoon);
    }
}
